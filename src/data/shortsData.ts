const getVideoUrl = (index: number) => sampleVideos[index % sampleVideos.length];
const getThumbnail = (index: number) => `https://i.pravatar.cc/400?img=${(index % 20) + 1}`;
const getAvatar = (index: number) => `https://i.pravatar.cc/100?img=${(index % 20) + 1}`;

// 키워드별 데이터 생성 함수
const createShortsForKeyword = (
  keyword: string,
  startId: number,
  count: number,
  titles: string[],
  descriptions: string[],
  authors: string[]
): ShortsItem[] => {
  return Array.from({ length: count }, (_, i) => ({
    id: `${keyword}-${startId + i}`,
    title: titles[i % titles.length] || `${keyword} 관련 콘텐츠 ${i + 1}`,
    description: descriptions[i % descriptions.length] || `${keyword}에 대한 흥미로운 내용입니다`,
    videoUrl: getVideoUrl(startId + i),
    thumbnail: getThumbnail(startId + i),
    author: authors[i % authors.length] || `${keyword}전문가`,
    authorAvatar: getAvatar(startId + i),
    likes: Math.floor(Math.random() * 5000) + 100,
    views: Math.floor(Math.random() * 100000) + 1000,
    comments: Math.floor(Math.random() * 500) + 10,
    duration: `${Math.floor(Math.random() * 2)}:${String(Math.floor(Math.random() * 60)).padStart(2, "0")}`,
    keywords: [keyword],
  }));
};

